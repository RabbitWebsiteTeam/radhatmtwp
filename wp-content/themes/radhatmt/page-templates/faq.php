		   <p><b>Yield stress:</b> Is the stress a TMT can withstand without permanent deformation., Yield strength is usually stated as the stress at which a permanent deformation of 0.2% of the original dimension will result, known as the 0.2% yield stress.
OR
The stress at which a non-proportional elongation equals to 0.2 % of the original gauge length takes place (where gauge length is 5.65sqrt A. ’A’ is the cross sectional area of the test piece).
</p>
<p><b>Ultimate Tensile Strength:</b> The maximum stress a material can withstand in a tensile test.
OR
The maximum load (force) reached in a tensile test divided by the effective cross sectional area of gauge length portion of the test piece.

</p>
  <p><b>Percentage Elongation:</b> When a material is tested for tensile strength it elongates a certain amount before fracture takes place. The two pieces are placed together and the amount of extension is measured against marks made before starting the test which is expressed as a percentage of the original gauge length.

